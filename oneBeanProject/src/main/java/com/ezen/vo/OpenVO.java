package com.ezen.vo;

public class OpenVO {
	private int openIdx;
	private int midx;
	private String openName;
	private int openPhone;
	private String openDate;
	private String openEmail;
	private char openCallYN;
	
	public int getOpenIdx() {
		return openIdx;
	}
	public void setOpenIdx(int openIdx) {
		this.openIdx = openIdx;
	}
	public int getMidx() {
		return midx;
	}
	public void setMidx(int midx) {
		this.midx = midx;
	}
	public String getOpenName() {
		return openName;
	}
	public void setOpenName(String openName) {
		this.openName = openName;
	}
	public int getOpenPhone() {
		return openPhone;
	}
	public void setOpenPhone(int openPhone) {
		this.openPhone = openPhone;
	}
	public String getOpenDate() {
		return openDate;
	}
	public void setOpenDate(String openDate) {
		this.openDate = openDate;
	}
	public String getOpenEmail() {
		return openEmail;
	}
	public void setOpenEmail(String openEmail) {
		this.openEmail = openEmail;
	}
	public char getOpenCallYN() {
		return openCallYN;
	}
	public void setOpenCallYN(char openCallYN) {
		this.openCallYN = openCallYN;
	}
}
