package com.ezen.vo;

public class RegularVO {
	private int regIdx;
	private String regName;
	private int regPrice;
	private int regStock;
	private String regDate;
	private char regDel;
	private String regImg;
	private String regDetailImg;
	
	
	public int getRegIdx() {
		return regIdx;
	}
	public void setRegIdx(int regIdx) {
		this.regIdx = regIdx;
	}
	public String getRegName() {
		return regName;
	}
	public void setRegName(String regName) {
		this.regName = regName;
	}
	public int getRegPrice() {
		return regPrice;
	}
	public void setRegPrice(int regPrice) {
		this.regPrice = regPrice;
	}
	public int getRegStock() {
		return regStock;
	}
	public void setRegStock(int regStock) {
		this.regStock = regStock;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public char getRegDel() {
		return regDel;
	}
	public void setRegDel(char regDel) {
		this.regDel = regDel;
	}
	public String getRegImg() {
		return regImg;
	}
	public void setRegImg(String regImg) {
		this.regImg = regImg;
	}
	public String getRegDetailImg() {
		return regDetailImg;
	}
	public void setRegDetailImg(String regDetailImg) {
		this.regDetailImg = regDetailImg;
	}	
}
